import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# Preparación de Datos (Usando tu variable 'data')
X_rf = data.drop(columns=['User_ID', 'Calories'])
y_rf = data['Calories']

# División de los Datos (Train/Test Split al 20%)
X_train_rf, X_test_rf, y_train_rf, y_test_rf = train_test_split(X_rf, y_rf, test_size=0.2, random_state=42)

# Creación y Entrenamiento del Modelo
# n_estimators=100 significa que creamos un bosque de 100 árboles trabajando en equipo
modelo_rf = RandomForestRegressor(n_estimators=100, random_state=42)
modelo_rf.fit(X_train_rf, y_train_rf)

print("======================================================")
print("   ¡RANDOM FOREST REGRESSOR ENTRENADO EXITOSAMENTE!")
print("======================================================")

"""##**Evaluación y Cálculo de Métricas de Rendimiento**"""

# Evaluación del Modelo (Métricas Oficiales)
y_pred_rf = modelo_rf.predict(X_test_rf)

mae_rf = mean_absolute_error(y_test_rf, y_pred_rf)
mse_rf = mean_squared_error(y_test_rf, y_pred_rf)
rmse_rf = mse_rf ** 0.5
r2_rf = r2_score(y_test_rf, y_pred_rf)

print(f"MAE (Error Absoluto Medio): {mae_rf:.4f} Kcal")
print(f"RMSE (Raíz del Error Cuadrático Medio): {rmse_rf:.4f} Kcal")
print(f"R² Score (Precisión del modelo): {r2_rf:.4f} ({r2_rf*100:.2f}%)")
print("-" * 50)

"""##**Extracción y Visualización de la Importancia de Atributos**"""

